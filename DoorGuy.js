// Door Guy

let years = [2001, 1973, 1981, 1991, 2014]

function canDrink(years) {
    let results = []
    for (i = 0; i < years.length; i++) {
        let age = 2019 - years[i]
        if (age >= 21) {
            results.push(true)
        } else {
            results.push(false)
        }
    }
    return results
}

console.log(canDrink(years))

